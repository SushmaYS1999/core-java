class Furniture{
	static String furnitureName;
	static int price;
	static String furnitureColor;
	
	static void setFurnitureName(String name)
	{
		furnitureName=name;
	}
	static String getFurnitureName()
	{
		return furnitureName;
	}
	static void setPrice(int Price)
	{
		price=Price;
	}
	static int getPrice()
	{
		return price;
	}
	static void setFurnitureColor(String color)
	{
		furnitureColor=color;
	}
	static String getFurnitureColor()
	{
		return furnitureColor;
	}
}