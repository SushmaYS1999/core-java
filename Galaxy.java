class Galaxy{
	static String galaxyName;
	static String galaxyType;
	static String numberOfStars;
	
	static void setGalaxyName(String name)
	{
		galaxyName=name;
	}
	static String getGalaxyName()
	{
		return galaxyName;
	}
	static void setGalaxyType(String type)
	{
		galaxyType=type;
	}
	static String getGalaxyType()
	{
		return galaxyType;
	}
	static void setNumberOfStars(String stars)
	{
		numberOfStars=stars;
	}
	static String getNumberOfStars()
	{
		return numberOfStars;
	}

}