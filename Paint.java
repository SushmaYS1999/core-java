class Paint{
	static int paintId;
	static String brandName;
	static String color;
	
		static void setPaintId(int id){
		paintId = id;
	}
		static int getPaintId(){
		return paintId;
	}
	static void setBrandName(String name){
		brandName = name;
	}
		static String getBrandName(){
		return brandName;
	}
	static void setColor(String paintColor){
		color = paintColor;
	}
		static String getColor(){
		return color;
	}
		

}