class RailwayStation{
	static String stationName;
	static String direction;
	static String trainName;
	
	static void setStationName(String name)
	{
		stationName=name;
	}
	static String getStationName()
	{
		return stationName;
	}
	static void setDirection(String enroute)
	{
		direction=enroute;
	}
	static String getDirection()
	{
		return direction;
	}
	static void setTrainName(String TrainName)
	{
		trainName=TrainName;
	}
	static String getTrainName()
	{
		return trainName;
	}

}