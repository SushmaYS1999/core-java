class Soil{
	static String soilName;
	static String soilColor;
	static String soilType;
	
	static void setSoilName(String name)
	{
		soilName=name;
	}
	static String getSoilName()
	{
		return soilName;
	}
	static void setSoilColor(String color)
	{
		soilColor=color;
	}
	static String getSoilColor()
	{
		return soilColor;
	}
	static void setSoilType(String type)
	{
		soilType=type;
	}
	static String getSoilType()
	{
		return soilType;
	}
}