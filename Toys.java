class Toys{
	static String toyName;
	static int toyPrice;
	static String toyColor;
	
	static void setToyName(String name)
	{
		toyName=name;
	}
	static String getToyName()
	{
		return toyName;
	}
	static void setToyPrice(int price)
	{
		toyPrice=price;
	}
	static int getToyPrice()
	{
		return toyPrice;
	}
	static void setToyColor(String color)
	{
		toyColor=color;
	}
	static String getToyColor()
	{
		return toyColor;
	}
}